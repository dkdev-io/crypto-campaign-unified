# Function to increment version
increment_version() {
    local version=$1
    local type=$2
    
    # Split version into major.minor.patch
    IFS='.' read -ra VERSION_PARTS <<< "$version"
    local major=${VERSION_PARTS[0]}
    local minor=${VERSION_PARTS[1]}
    local patch=${VERSION_PARTS[2]}
    
    case $type in
        patch)
            patch=$((patch + 1))
            ;;
        minor)
            minor=$((minor + 1))
            patch=0
            ;;
        major)
            major=$((major + 1))
            minor=0
            patch=0
            ;;
    esac
    
    echo "$major.$minor.$patch"
}

# Function to update package.json version
update_package_version() {
    local new_version=$1
    
    # Use node to update package.json
    node -e "
        const fs = require('fs');
        const pkg = JSON.parse(fs.readFileSync('package.json', 'utf8'));
        pkg.version = '$new_version';
        fs.writeFileSync('package.json', JSON.stringify(pkg, null, 2) + '\n');
    "
}

# Function to get commit messages since last tag
get_recent_changes() {
    local last_tag=$(git describe --tags --abbrev=0 2>/dev/null || echo "")
    
    if [ -z "$last_tag" ]; then
        # No previous tags, get all commits
        git log --oneline --pretty=format:"â€¢ %s" HEAD | head -20
    else
        # Get commits since last tag
        git log --oneline --pretty=format:"â€¢ %s" "$last_tag"..HEAD
    fi
}

# Function to create feature-based tag
create_feature_tag() {
    local version=$1
    local feature_name=$2
    local tag_name="v$version-$feature_name"
    
    echo -e "${BLUE}ğŸ·ï¸  Creating feature tag: ${CYAN}$tag_name${NC}"
    
    # Get changes for this release
    local changes=$(get_recent_changes)
    
    # Create annotated tag with changelog
    git tag -a "$tag_name" -m "Release $tag_name

ğŸ“‹ Changes in this release:
$changes

ğŸš€ Generated by Version Manager
ğŸ“… $(date '+%Y-%m-%d %H:%M:%S')"
    
    echo -e "${GREEN}âœ“ Created tag: $tag_name${NC}"
    return 0
}

# Function to create standard tag
create_standard_tag() {
    local version=$1
    local tag_name="v$version"
    
    echo -e "${BLUE}ğŸ·ï¸  Creating standard tag: ${CYAN}$tag_name${NC}"
    
    # Get changes for this release
    local changes=$(get_recent_changes)
    
    # Create annotated tag with changelog
    git tag -a "$tag_name" -m "Release $tag_name

ğŸ“‹ Changes in this release:
$changes

ğŸš€ Generated by Version Manager
ğŸ“… $(date '+%Y-%m-%d %H:%M:%S')"
    
    echo -e "${GREEN}âœ“ Created tag: $tag_name${NC}"
    return 0
}

# Main version bumping logic
case "${1:-help}" in
    patch)
        echo -e "${BLUE}ğŸ”§ Bumping patch version...${NC}"
        NEW_VERSION=$(increment_version "$CURRENT_VERSION" "patch")
        ;;
    minor)
        echo -e "${BLUE}ğŸ”§ Bumping minor version...${NC}"
        NEW_VERSION=$(increment_version "$CURRENT_VERSION" "minor")
        ;;
    major)
        echo -e "${BLUE}ğŸ”§ Bumping major version...${NC}"
        NEW_VERSION=$(increment_version "$CURRENT_VERSION" "major")
        ;;
    custom)
        echo -e "${CYAN}âœ¨ Custom Version Mode${NC}"
        echo ""
        echo "Options:"
        echo "  1) Set specific version (e.g., 2.1.0)"
        echo "  2) Create feature release (e.g., v2.0.0-crypto-ready)"
        echo ""
        read -p "Choose option (1-2): " option
        
        case $option in
            1)
                read -p "Enter new version (x.y.z): " NEW_VERSION
                if [[ ! $NEW_VERSION =~ ^[0-9]+\.[0-9]+\.[0-9]+$ ]]; then
                    echo -e "${RED}âŒ Invalid version format. Use x.y.z${NC}"
                    exit 1
                fi
                ;;
            2)
                read -p "Enter base version (x.y.z): " NEW_VERSION
                if [[ ! $NEW_VERSION =~ ^[0-9]+\.[0-9]+\.[0-9]+$ ]]; then
                    echo -e "${RED}âŒ Invalid version format. Use x.y.z${NC}"
                    exit 1
                fi
                read -p "Enter feature name (e.g., crypto-ready): " FEATURE_NAME
                if [ -z "$FEATURE_NAME" ]; then
                    echo -e "${RED}âŒ Feature name cannot be empty${NC}"
                    exit 1
                fi
                CUSTOM_FEATURE=true
                ;;
            *)
                echo -e "${RED}âŒ Invalid option${NC}"
                exit 1
                ;;
        esac
        ;;
    help|*)
        echo "Version Manager Usage:"
        echo ""
        echo "Automatic semantic versioning:"
        echo "  ./scripts/version.sh patch   - Bug fixes (1.0.0 â†’ 1.0.1)"
        echo "  ./scripts/version.sh minor   - New features (1.0.0 â†’ 1.1.0)"
        echo "  ./scripts/version.sh major   - Breaking changes (1.0.0 â†’ 2.0.0)"
        echo ""
        echo "Custom versioning:"
        echo "  ./scripts/version.sh custom  - Interactive custom version/feature"
        echo ""
        echo "NPM shortcuts:"
        echo "  npm run version:patch"
        echo "  npm run version:minor"
        echo "  npm run version:major"
        echo "  npm run version:custom"
        echo ""
        echo "Examples of feature releases:"
        echo "  v2.0.0-crypto-ready"
        echo "  v1.5.0-mobile-support"
        echo "  v3.0.0-beta"
        exit 0
        ;;
esac

# Show the change
